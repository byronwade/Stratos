import { autoTagJob } from "@/lib/ai/job-auto-tagger";
import type { Job } from "@/lib/db/schema";

// Helper function to create jobs with AI auto-tagging
// AI generates title/description from notes + tags everything for reporting
function createJobWithAI(
	jobData: Omit<
		Job,
		| "title"
		| "description"
		| "aiCategories"
		| "aiEquipment"
		| "aiServiceType"
		| "aiPriorityScore"
		| "aiTags"
		| "aiProcessedAt"
	> & {
		customerName: string;
	}
): Job {
	const aiMetadata = autoTagJob({
		customerName: jobData.customerName,
		notes: jobData.notes,
		jobType: jobData.jobType,
		priority: jobData.priority,
	});

	return {
		...jobData,
		title: aiMetadata.generatedTitle, // AI-generated, user-editable
		description: aiMetadata.generatedDescription, // AI-generated, user-editable
		aiCategories: aiMetadata.categories as unknown as string | null,
		aiEquipment: aiMetadata.equipment as unknown as string | null,
		aiServiceType: aiMetadata.serviceType,
		aiPriorityScore: aiMetadata.priorityScore,
		aiTags: aiMetadata.tags as unknown as string | null,
		aiProcessedAt: aiMetadata.processedAt,
	};
}

